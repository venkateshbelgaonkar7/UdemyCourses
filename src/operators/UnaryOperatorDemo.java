package operators;

public class UnaryOperatorDemo {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);

        int b = 10;
        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);

    }
}
