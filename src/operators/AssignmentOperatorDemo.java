package operators;

public class AssignmentOperatorDemo {
    public static void main(String[] args) {
        int b = 10, a = 20;
        a += b;
        System.out.println(a);
        a -= b;
        System.out.println(a);
        a /= b;
        System.out.println(a);
        a %= b;
        System.out.println(a);
        a *= b;
        System.out.println(a);
    }
}
